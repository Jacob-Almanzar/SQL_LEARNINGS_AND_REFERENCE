-- EXAMPLES
-- SHOW THE EMPLOYEE HIERARCHY BY DISPLAYING EACH EMPLOYEES LEVEL WITHIN THE ORGANIZATION
WITH HIERARCHY AS
(
SELECT 
EmployeeID,
FirstName,
ManagerID,
1 AS LEVEL
FROM Sales.Employees
WHERE ManagerID IS NULL

UNION ALL

SELECT
e.EmployeeID,
e.FirstName,
e.ManagerID,
LEVEL + 1
FROM SALES.Employees AS e
INNER JOIN HIERARCHY
ON e.ManagerID = HIERARCHY.EmployeeID
)

SELECT
*
FROM HIERARCHY
