 --EXAMPLE
 -- STEP 1: FIND THE TOTAL SALES PER CUSTOMER
	-- FIRST WRITE THE QUEREY 
	-- THEN NAME IT INTO A CTE
WITH CTE_TOTAL_SALES AS
(
 SELECT
	 CustomerID,
	 SUM(SALES) AS TOTAL_SALES
 FROM SALES.ORDERS
 GROUP BY CustomerID
)
--STEP 2: JOIN THE CTE INTO THE MAIN QUERY 
-- MAIN QUEREY
SELECT
c.CustomerID,
c.FirstName,
c.LastName,
cts.TOTAL_SALES
FROM SALES.Customers c
LEFT JOIN CTE_TOTAL_SALES cts --> JOINING THE CTE INTO THE MAIN QUERY 
ON cts.CustomerID = c.CustomerID
ORDER BY CustomerID


