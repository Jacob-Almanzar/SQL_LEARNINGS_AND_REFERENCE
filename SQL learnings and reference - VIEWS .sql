-- WHAT ARE VIEWS?
	-- VIRTURAL TABLE BASED ON THE RESULT SET OF A QUERY, WITHOUT STORING THE DATA IN DATABASE.
	-- VIEWS ARE PRESISTED SQL QUERIES IN THE DATABASE IN THE DATABASE

-- TABLES VS VIEWS
	--TABLES HOME DATA
		-- VIEWS ARE VIRTUAL, DONT HOLD DATA
	--VIEWS ARE EASIER TO MAINTAIN
	-- TABLES ARE FASTER THAN VIEWS
		-- VIEWS ARE TWO QUEIRIES
			-- ONE TO POPULATE THE VIEW, AND THE OTHER TO GET RESULTS
	--READ/WRITE WITH TABLES
		-- READ ONLY WITH VIEWS

-- VIEWS VS CTE
 -- CTE REDUCE REFUNDANCY IN 1 QUERY, IMPROVES REUSABILITY IN 1 QUERY, IS TEMPORARY LOGIC, NO MAINTENANCE (AUTO CLEANUP)
	-- VIEWS REDUCE REDUNDANCY IN MULTIPLE QUERIES, IMPROVES REUSABILITY IN MULITPLE QUERIES, PERSISTED LOGIC, NEED TO MAINTAIN (CREATE/DROP) 
