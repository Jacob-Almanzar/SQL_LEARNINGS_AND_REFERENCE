-- FIND THE RUNNING TOTAL OF SALES FOR EACH MONTH
CREATE VIEW SALES.V_MONTHLY_SUM_SALES AS
(
SELECT
MONTH(ORDERDATE) AS ORDER_MONTH,
SUM(SALES)  AS MONTHLY_TOTAL_SALES,
COUNT(ORDERID) AS TOTAL_ORDERS,
SUM(QUANTITY) AS TOTAL_QUANTITY
FROM SALES.Orders
GROUP BY MONTH(ORDERDATE)
)

SELECT
ORDER_MONTH,
SUM(MONTHLY_TOTAL_SALES) OVER (ORDER BY ORDER_MONTH) AS RUNNING_TOTAL
FROM SALES.V_MONTHLY_SUM_SALES


-- USE CASE 2 : HIDE COMPLEXITY
	-- PROVIDE A CLEAN VIEW FOR END USERS 

-- EXAMPLE
-- PROVIDE A VIEW THE COMBINES DETAILS FROM ORDERS, PRODUCTS, CUSTOMERS, AND EMPLOYEES


CREATE VIEW SALES.v_ORDER_DETAILS AS (
SELECT 
O.OrderID,
P.Product,
P.Category,
O.OrderDate,
O.Sales,
O.Quantity,
COALESCE(C.FirstName, '')+ ' ' +COALESCE(C.LastName,'') AS CUSTOMER,
C.Country AS CUSTOMER_COUNTRY,
COALESCE(E.FirstName, '')+ ' ' +COALESCE(E.LastName,'') AS EMPLOYEE,
E.Department
FROM SALES.Orders o
LEFT JOIN SALES.Products p
ON o.ProductID = p.ProductID
LEFT JOIN SALES.Customers c
ON o.CustomerID = c.CustomerID
LEFT JOIN Sales.Employees e
ON o.SalesPersonID = e.EmployeeID
)

--USE CASE 3: SECURITY
-- USE A VIEW TO ENFORCE SECURITY AND PROTECT SENSITVE DATA, BY HIDING COLUMNS AND/OR ROWS FROM TABLES.
-- CAN RESTRICT WHAT COLUMNS AND/OR WHAT ROWS CAN BE SEEN BY CREATING VIEWS 


-- EXAMPLE: PROVIDE A VIEW FOR THE EU SALES TEAM THAT COMBINES DETAILS FROM ALL TABLES AND EXCLUDES DATA RELATED TO THE USE

CREATE VIEW SALES.EU_ORDERS_DETAILS AS (
SELECT 
O.OrderID,
P.Product,
P.Category,
O.OrderDate,
O.Sales,
O.Quantity,
COALESCE(C.FirstName, '')+ ' ' +COALESCE(C.LastName,'') AS CUSTOMER,
C.Country AS CUSTOMER_COUNTRY,
COALESCE(E.FirstName, '')+ ' ' +COALESCE(E.LastName,'') AS EMPLOYEE,
E.Department
FROM SALES.Orders o
LEFT JOIN SALES.Products p
ON o.ProductID = p.ProductID
LEFT JOIN SALES.Customers c
ON o.CustomerID = c.CustomerID
LEFT JOIN Sales.Employees e
ON o.SalesPersonID = e.EmployeeID
WHERE c.Country != 'USA'
)

SELECT *
FROM SALES.EU_ORDERS_DETAILS


