 -- THE ORDER OF THE COLUMNS IN EACG QUERY MUST BE THE SAME

 -- COLUMN ALIASES
	-- THE COLUMNS NAME IN THE RESULT SET ARE DETERMINED BY THE COLUMN NAME SPECIFIED IN THE FIRST QUERY
		-- FIRST COLUMN DETERMINS COLUMN NAMES

--CORRECT COLUMNS
	-- EVEN IF ALL RULES ARE MET AND SQL SHOWS NO ERRORS, THE RESULTS MAY BE INCORRECT
