-- UNION
-- RETURNS ALL DISTINCT ROWS FROM BOTH QUERIES
	-- REMOVES ALL DUPLICATES
--COMBINE THE DATA FROM EMPLOYEES AND CUSTOMERS INTO ONE TABLE
SELECT 
EmployeeID AS ID,
FirstName AS FIRST_NAME,
LastName AS LAST_NAME
FROM SALES.Employees

	UNION

SELECT 
CustomerID,
FirstName,
LastName
FROM SALES.Customers

-- UNION ALL
-- RETURNS ALL ROWS FROM BOTH QUERIES, INCLUDING DUPLICATES

--COMBINE THE DATA FROM EMPLOYEES AND CUSTOMERS INTO ONE TABLE, INCLUDING DUPLICATES

SELECT 
EmployeeID AS ID,
FirstName AS FIRST_NAME,
LastName AS LAST_NAME
FROM SALES.Employees

	UNION ALL

SELECT 
CustomerID,
FirstName,
LastName
FROM SALES.Customers


-- EXCEPT
-- RETURNS ALL DISTINCT ROWS FROM THE FIRST QUERY, THAT ARE NOT FOUDN IN THE SECOND QUERY 

-- RETURN ALL EMPLOYEES WHO ARE NOT CUSTOMERS
	-- ROWS IN THE EMPLOYEES SET THAT ARE NOT IN THE CUSTOMER SET
SELECT 
EmployeeID AS ID,
FirstName AS FIRST_NAME,
LastName AS LAST_NAME
FROM SALES.Employees

	EXCEPT

SELECT 
CustomerID,
FirstName,
LastName
FROM SALES.Customers


-- INTERSECT
-- RETURNS ROWS THAT ARE COMMON IN BOTH QUERIES, REMOVES DUPLICATES

-- FIND THE EMPLOYEES WHO ARE ALSO CUSTOMERS

SELECT
EmployeeID AS ID,
FirstName AS FIRST_NAME,
LastName AS LAST_NAME
FROM SALES.Employees

INTERSECT

SELECT 
CustomerID,
FirstName,
LastName
FROM SALES.Customers


-- PRACTICE
-- ORDERS ARE STORED IN SEPERATE TABLES ( ORDERS & ORDERESARCHIVE)
-- COMBINE ALL ORDERS INTO ONE REPORT WITHOUT DUPLICATES

