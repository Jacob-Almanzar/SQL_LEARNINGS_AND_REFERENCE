-- show creationtime using the following format:
-- day wed jan q1 2025 12:34:56PM
SELECT
CREATIONTIME,
'DAY ' + FORMAT(CREATIONTIME,'ddd MMM') + CONCAT(' q',DATEPART(QUARTER, CREATIONTIME)) + FORMAT(CREATIONTIME,' yyyy  hh:mm:ss tt')
FROM SALES.Orders


-- DATE AGGREGATIONS
SELECT
FORMAT(ORDERDATE, ' MMM - yy') AS ORDERDATE,
COUNT(*) AS NUMBER_OF_ORDERS
FROM SALES.Orders
GROUP BY FORMAT(ORDERDATE, ' MMM - yy')


-- CONVERT
 -- CONVERST DATE AND TIME TO ANOTHER VALUE
 
 SELECT 
 CONVERT(INT, '123') AS STR_TO_INT,
 CONVERT(DATE, '2025-08-20') AS STR_TO_DATE,
 CREATIONTIME,
 CONVERT(DATE, CREATIONTIME) AS DATETIME_TO_DATE,
 CONVERT(VARCHAR, CREATIONTIME, 32) AS USA_STD32,
 CONVERT(VARCHAR, CREATIONTIME, 34) AS EURO_STD34
 FROM SALES.Orders

 -- CAST
 SELECT
 CAST('2020-08-20' AS DATE) AS STR_TO_DATE,
  CAST('2020-08-20' AS DATETIME) AS STR_TO_DATETIME,
   CAST(CREATIONTIME AS DATE) AS DATETIME_TO_DATE,
 CAST('123' AS INT) AS STR_TO_INT,
 CAST(123 AS VARCHAR) AS INT_TO_STR
 FROM SALES.Orders


 -- DATE ADD
	-- ADDS OR SUBTRACTS A SPECIFIC TIME INTERVAL TO/FROM A DATE
SELECT
orderid,
orderdate,
dateadd(year, 2, orderdate) as TWO_YEAR_JUMP,
DATEADD(MONTH, 3, ORDERDATE) AS '3_MONTH_JUMP',
DATEADD(DAY, -10, ORDERDATE) AS '10 DAY BACK'
from sales.Orders

--DATEDIFF
	-- FIND THE DIFFERENCE BETWEEN TWO DATES

SELECT 
ORDERDATE,
SHIPDATE,
DATEDIFF(DAY, ORDERDATE, ShipDate) AS LAG_DAYS
FROM SALES.Orders

-- CALCUALTE THE AGE OF EMPLOYEES
SELECT
FIRSTNAME,
BIRTHDATE,
DATEDIFF(YEAR, BIRTHDATE, GETDATE()) AS AGE
FROM SALES.Employees

-- FIND THE AVERAGE SHIPPING DURATION IN DAYS FOR EACH MONTH 
SELECT
FORMAT(ORDERDATE, 'MMM') AS MONTH,
AVG(DATEDIFF(DAY, OrderDate, ShipDate)) AS AVG_SHIPPING_DURATION
FROM SALES.ORDERS
GROUP BY FORMAT(ORDERDATE, 'MMM')


-- TIME GAP ANALYSIS
-- FIND THE NUMBER OF DAYS BETWEEN EACH ORDER AND THE PREVIOUS ORDER
SELECT
ORDERID,
ORDERDATE AS CURRENT_ORDERDATE,
LAG(ORDERDATE) OVER (ORDER BY ORDERDATE) AS PREVIOUS_ORDERDATE,
DATEDIFF(DAY, ORDERDATE, LAG(OrderDate) OVER (ORDER BY ORDERDATE)) AS DIFF_BETWEEN_ORDERDATE
FROM SALES.ORDERS


