 SELECT 
 GETDATE() AS FULL_DATE,
 DAY(GETDATE()) AS DAY,
 MONTH(GETDATE()) AS MONTH,
 YEAR(GETDATE()) AS YEAR,

 --DATEPART()
	--DATEPART(PART, DATE)
	 --PART
		--month, week, quarter

select 
datepart(year, CreationTime) YEAR,
datepart(week, CreationTime) WEEK,
datepart(month, CREATIONTIME) MONTH,
datepart(quarter, creationtime) QUARTER,
DATEPART(HOUR, CreationTime) HOUR,
DATEPART(MINUTE, CREATIONTIME) MINUTE,
DATEPART(SECOND, CREATIONTIME) SECOND
from sales.Orders


--DATENAME()
	-- RETURNS THE NAME OF THE DATEPART
SELECT 
DATENAME(MONTH, CreationTime) MONTH,
DATENAME(YEAR, CREATIONTIME) YEAR,
DATENAME(DAY, CREATIONTIME) DAY
FROM SALES.Orders


-- DATETRUNC()
 -- GIVVE THE DATETIME FOR A VALUE, BUT RESETS ALL VALUES AFTER THE DEFIND END POINT WILL BE 0
SELECT
DATETRUNC(HOUR, CREATIONTIME)
FROM SALES.Orders


--EOMONTH()
	-- RETURNS THE LAST DAY OF A MONTH

SELECT
ORDERID,
CREATIONTIME,
EOMONTH(CREATIONTIME) AS LASTDAYOFMONTH,
DAY(EOMONTH(CREATIONTIME)) AS LAST_DAY,
DAY(DATETRUNC(MONTH, CREATIONTIME)) AS FIRST_DAY,
CAST(DATETRUNC(MONTH, CREATIONTIME) AS DATE) AS STARTOFMONTH
FROM SALES.ORDERS


-- HOW MANY ORDERS WE PLACED EACH YEAR
SELECT
COUNT(*) AS NBROFORDERS,
YEAR(OrderDate) AS YEAR
FROM SALES.Orders
GROUP BY YEAR(OrderDate)

-- HOW MANY ORDERS PLACED PER MOMTH 
SELECT
DATENAME(MONTH,OrderDate) AS MONTH,
COUNT(*) AS NBR_OF_ORDER
FROM SALES.ORDERS
GROUP BY  DATENAME(MONTH,OrderDate)

-- SHOW ALL ORDERS PLACED DURING THE MONTH OF FEBURARY
SELECT 
DATENAME(MONTH, ORDERDATE) AS MONTH,
COUNT(*) AS NUM_OF_ORDERS
FROM SALES.ORDERS
WHERE (DATENAME(MONTH,ORDERDATE)) = 'FEBRUARY'
GROUP BY DATENAME(MONTH,ORDERDATE)



