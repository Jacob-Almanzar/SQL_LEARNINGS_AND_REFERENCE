-- FIND THE TOTAL SALES ACROSS ALL ORDERS, ADDITIONALL PROVIDE DETAILS SUCH AS ORDER ID AND ORDER DATE
SELECT
ORDERID,
ORDERDATE,
SUM(SALES) OVER ( ) AS TOTAL_SALES
FROM SALES.Orders

-- FIND THE TOTAL SALES ACROSS ALL ORDERS FOR EACH PRODUCT, ADDITIONALL PROVIDE DETAILS SUCH AS ORDER ID AND ORDER DATE (1)
-- ADD IN TOTAL SALES FOR ALL ORDERS (2)
-- ADD IN BY ORDER STATUS (3)
