--WINDOW FUNCTIONS
-- FUCTIONS THAT PERFORM CALCULATIONS ON A SPECIFIC SUBSET OF DATA WITHOUT LOSING THE LEVEL OF DETAILS OF ROWS

--FIND THE TOTAL SALES ACROSS ALL ORDERS
SELECT
SUM(SALES)
FROM SALES.Orders;
SELECT 
ORDERID,
SALES,
SUM(SALES) OVER () AS TOTAL_SALES
FROM SALES.ORDERS

-- FIND THE TOTAL SALES FOR EACH PRODUCT
SELECT
PRODUCTID,
SUM(SALES) AS TOTAL_SALES
FROM SALES.Orders
GROUP BY ProductID

-- FIND THE TOTAL SALES FOR EACH PRODUCT (INCLUDE ORDER ID AND ORDERDATE)
SELECT
	ORDERID,
	ORDERDATE,
	PRODUCTID,
	SUM(SALES) OVER (PARTITION BY PRODUCTID) AS TOTAL_SALES
FROM SALES.Orders

-- FIND THE TOTAL SALES AND SALES PER ORDER, FOR EACH PRODUCT
SELECT
	ORDERID,
	PRODUCTID,
	SALES AS ORDER_SALES,
	SUM(SALES) OVER (PARTITION BY PRODUCTID) AS TOTA_PRODUCT_SALES
FROM SALES.Orders
--

-- sum of sales per month by product
select distinct
datename(month,orderdate) as month,
productid,
sum(sales) over (partition by month(orderdate), productid) as Total_sales_per_month
from sales.Orders

