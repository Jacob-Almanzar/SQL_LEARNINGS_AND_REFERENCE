-- FIND THE PERCENTAGE CONTROBUTION OF EACH PRODUCT SALES TO THE TOTAL SALES
SELECT DISTINCT
PRODUCTID,
SUM(SALES) OVER () AS TOTAL_SALES,
SUM(SALES) OVER (PARTITION BY PRODUCTID) AS PRODUCT_SALES,
(SUM(CAST(SALES AS FLOAT)) OVER (PARTITION BY PRODUCTID) / SUM(SALES) OVER ())*100  AS CONTRIBUTION
FROM SALES.ORDERS


--AVERAGE
SELECT DISTINCT
PRODUCTID,
COUNT(*) OVER (PARTITION BY PRODUCTID) AS NUMBER_OF_ORDERS,
AVG(COALESCE(SALES,0)) OVER (PARTITION BY PRODUCTID) AS AVG_PRODUCT_SALES -- USE COALESCE TO HANDLE ANY NULL VALUES AND CONVERT THEM TO 0
FROM SALES.ORDERS

-- FIND AVG SALES ACROSS ALL ORDER, FOR EACH PRODUCT, INCLUDE ORDER ID NA DORDER DATE
SELECT
ORDERID,
ORDERDATE,
PRODUCTID,
AVG(SALES) OVER () AS AVG_ORDERS,
AVG(SALES) OVER (PARTITION BY PRODUCTID) AS AVG_SALES_PRODUCT
FROM SALES.ORDERS

-- FIND THE AVERAGE SCORE OF CUSTOMERS, INCLUDE CUSTOMER ID AND LASTNAME
SELECT
CUSTOMERID,
LASTNAME,
AVG(COALESCE(SCORE,0)) OVER () AS AVG_SCORE
FROM SALES.Customers

-- FIND ALL ORDERS WHERE SALES ARE HIGHER THAN THE AVERAGE SALES ACROSS ALL ORDERS
SELECT
ORDERID,
SALES,
AVG_SALES
FROM (
	SELECT 
	*,
	AVG(SALES) OVER () AS AVG_SALES
	FROM SALES.Orders
)t
WHERE SALES > AVG_SALES

-- MIN AND MAX
-- FIND THE HIGHEST AND LOWEST SALES ACROSS ALL ORDERS, AND FOR EACH PRODUCT, PROVIDE ORDER ID AND ORDER DATE
SELECT
ORDERID,
ORDERDATE,
PRODUCTID,
MAX(SALES) OVER () AS MAX_SALES,
MIN(COALESCE(SALES,0)) OVER () AS MIN_SALES,
MAX(SALES) OVER (PARTITION BY PRODUCTID) AS MAX_PRODUCT_SALES,
MIN(COALESCE(SALES,0)) OVER (PARTITION BY PRODUCTID) AS MIN_PRODUCT_SALES
FROM SALES.Orders

-- CALCULATE THE DEVIATION OF EACG SALE FROM BOTH THE MIN AND MAX SALES AMOUNT
SELECT
*,
SALES - MIN_SALES AS LOWER_DEVIATION,
MAX_SALES - SALES AS UPPER_DEVIATION
FROM(
	SELECT
	*,
	MIN(SALES) OVER () AS MIN_SALES,
	MAX(SALES) OVER () AS MAX_SALES
	FROM SALES.Orders
)t

-- RUNNING AND ROLLING TOTALS 
-- TRACKING CURRENT SALES WITH TARGET SALES
-- PROVIODING INSIGHTS INTO HISTORICAL PATTERNS
-- RUNNING TOTAL - AGGREAGETE ALL VALYES FROM THE BEGINING UP TO THE CURRENT POINT
-- ROLLING TOTAL - AGGREGATE ALL VALUES WITHIN A FIXED WINDOW (30DAYS). AS NEW DATA IS ADDED, THE OLDEST POINT WILL BE DROPPED

-- CALCULATE THE MOVING AVERAGE OF SALES FOR EACH PRODUCT OVER TIME
SELECT 
ORDERDATE,
PRODUCTID,
SALES,
AVG(SALES) OVER (PARTITION BY PRODUCTID ORDER BY ORDERDATE) AS MOVING_SALES_AVG_PRODUCTID
FROM SALES.Orders
-- CALCULATE THE MOVING AVERAGE OF SALES FOR EACH PRODUCT OVER TIME, ONLY INCLUDING THE NEXT ORDER
SELECT 
ORDERDATE,
PRODUCTID,
SALES,
AVG(SALES) OVER (PARTITION BY PRODUCTID ORDER BY ORDERDATE ROWS BETWEEN CURRENT ROW AND 1 FOLLOWING) AS ROLLING_SALES_AVG_PRODUCTID
FROM SALES.Orders


-- RANKING FUNCTIONS
-- RANK () OVER ((OPTIONAL) PARTITION BY XYZ (REQUIRED) ORDER BY ZYX)

-- ROW_NUMBER() -> IGNORES TIES
--RANK THE ORDERS BASED ON THEIR SALES FROM THE HIGHEST TO LOWEST
SELECT
ORDERID,
SALES,
ROW_NUMBER() OVER (ORDER BY SALES DESC) AS RANK
FROM SALES.Orders

--RANK -> DOES NOT IGNORE TIES AND WILL LEAVE 'GAPS' IN RANK IF THERE ARE TIES
--RANK THE ORDERS BASED ON THEIR SALES FROM THE HIGHEST TO LOWEST
SELECT
ORDERID,
SALES,
RANK() OVER (ORDER BY SALES DESC) AS RANK
FROM SALES.Orders

--DENSE_RANK
-- SAME AS RANK, BUT WILL NOT LEAVE GAPS
--RANK THE ORDERS BASED ON THEIR SALES FROM THE HIGHEST TO LOWEST
SELECT
ORDERID,
SALES,
DENSE_RANK() OVER (ORDER BY SALES DESC) AS RANK
FROM SALES.Orders

