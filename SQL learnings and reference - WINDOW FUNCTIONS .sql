-- VALUE FUNCTIONS
-- LEAD (EXPRESSION, OFFSET (OPTIONAL, NUMBER OF ROWS FORARD OR BACKWARDS FROM CURRENT ROW), DEFAULT VALUE (IF THERES NO VALUE/NULL)) ACCESS A VALUE FROM THE NEXT ROW WITHIN A WINDOW 
-- LAG()  ACCESS A VALUE FROM THE PREVIOUS ROW WITHIN A WINDOW
SELECT 
PRODUCTID,
ORDERDATE,
SALES,
LEAD(SALES,2,0) OVER (PARTITION BY PRODUCTID ORDER BY ORDERDATE) AS TWO_FORWARD_BY_PRODUCT,
LEAD(SALES,2,0) OVER (ORDER BY ORDERDATE) AS TWO_FORWARD,
LAG(SALES,2,0) OVER (ORDER BY ORDERDATE) AS TWO_PREVIOUS,
LEAD(SALES) OVER (ORDER BY ORDERDATE) AS ONE_FORWARD,
LAG(SALES) OVER (ORDER BY ORDERDATE) AS ONE_PREVIOUS
FROM SALES.ORDERS

-- ANALYSE THE MONTH-OVER-MONTH PERFORMANCE BY FINDING THE PERCENTAGE CHANGE IN SALES BETWEEN THE CURRENT AND PREVIOUS MONTH 
		--MY ATTEMPT
SELECT
*,
CONCAT(((CAST(TOTAL_MONTH_SALES AS FLOAT) - PREVIOUS_MONTH_SALES)/PREVIOUS_MONTH_SALES) * 100,'%') AS PERCENT_CHANGE_MONTH_OVER_MONTH
FROM(
	SELECT 
	*,
	LAG(TOTAL_MONTH_SALES,1) OVER (ORDER BY ORDER_MONTH) AS PREVIOUS_MONTH_SALES
	FROM ( 
		SELECT DISTINCT
		MONTH(ORDERDATE) AS ORDER_MONTH,
		SUM(SALES) OVER (PARTITION BY MONTH(ORDERDATE) ORDER BY MONTH(ORDERDATE)) AS TOTAL_MONTH_SALES
		FROM SALES.Orders
	)t
)t


		-- EASIER AND CLEANER WAY
SELECT
*,
CONCAT((CAST(CURRENT_MONTH_SALES AS FLOAT) - PREVIOUS_MONTH_SALES) / PREVIOUS_MONTH_SALES * 100, '%') AS 'M-O-M_DELTA'
FROM (
	SELECT
	 MONTH(ORDERDATE) AS ORDER_MONTH,
	 SUM(SALES) AS CURRENT_MONTH_SALES,
	 LAG(SUM(SALES)) OVER (ORDER BY MONTH(ORDERDATE)) AS PREVIOUS_MONTH_SALES
