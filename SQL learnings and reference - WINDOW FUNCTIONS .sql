--

--RANK EACH ORDER BASED ON THEIR SALES FROM HIGHEST TO THE LOWEST, ADDITIONALLY PROVIDE DETAILS SUCH AS ORDER ID AND ORDER DATE
SELECT
ORDERID,
ORDERDATE,
SALES,
RANK() OVER (ORDER BY SALES DESC) AS RANK 
FROM SALES.Orders

-- WINDOW FRAME
--DEFINES A SUBSET OF ROWS WITHIN EACH WINDOW THAT IS RELEVANT FOR THE CALCULATION
SELECT
ORDERID,
ORDERDATE,
ORDERSTATUS,
SALES,
SUM(SALES) OVER (PARTITION BY ORDERSTATUS ORDER BY ORDERDATE ROWS BETWEEN CURRENT ROW AND 2 FOLLOWING ) AS TOTAL_SALES
FROM SALES.Orders

-- FIND THE TOTAL SALES FOR EACH ORDER STATUS, BUT ONLY FOR PRODUCTS 101 AND 102
SELECT
DISTINCT
ProductID,
ORDERSTATUS,
SUM(SALES) OVER (PARTITION BY PRODUCTID, ORDERSTATUS) AS TOTAL_SALES
FROM SALES.ORDERS
WHERE PRODUCTID = '101' OR PRODUCTID =  '102'

