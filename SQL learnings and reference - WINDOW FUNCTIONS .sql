	 GROUP BY MONTH(ORDERDATE)
)t

--CUSTOMER RETENTION ANALYSIS
-- ANALYZE CUSTOMER LOYALTY BY RANKING CUSTOMERS BASED ON THE AVERAGE NUMBER OF DAYS BETWEEN ORDERS
SELECT
*,
ROW_NUMBER() OVER (ORDER BY COALESCE((AVG_DAYS_BETWEEN_ORDERS), 999999999 ) ASC) AS RANKING
FROM(
	SELECT DISTINCT 
	CUSTOMERID,
	AVG(-DAYS_BETWEEN_ORDERS) OVER (PARTITION BY CUSTOMERID) AS AVG_DAYS_BETWEEN_ORDERS
	FROM (
		SELECT
		CUSTOMERID,
		ORDERID,
		OrderDate,
		LAG(ORDERDATE) OVER (PARTITION BY CUSTOMERID ORDER BY ORDERDATE ASC) AS PREVIOUS_ORDER_DATE,
		DATEDIFF(DAY,ORDERDATE,LAG(ORDERDATE) OVER (PARTITION BY CUSTOMERID ORDER BY ORDERDATE ASC)) AS DAYS_BETWEEN_ORDERS
		FROM SALES.Orders
	)t
)t

-- FIRST_VALUE() 
	-- ACCESS A VALUE FROM THE FIRST ROW WITHIN A WINDOW
-- LAST_VALUE()
	-- ACCESS A VALUE FROM THE FIRST ROW WITHIN A WINDOW

-- FIND THE HIGHEST AND LOWEST SALES PER PRODUCT
SELECT DISTINCT
PRODUCTID,
COUNT(SALES) OVER (PARTITION BY PRODUCTID) AS NUMBER_OF_SALES,
FIRST_VALUE(Sales) OVER (PARTITION BY PRODUCTID ORDER BY SALES ASC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING ) AS LOWEST_SALE,
LAST_VALUE(SALES) OVER (PARTITION BY PRODUCTID ORDER BY SALES ASC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS HIGHEST_SALE
FROM SALES.Orders

-- NOW FIND THE DIFFERENCE BETWEEN THE CURRENT SALE AND THE LOWEST/ HIGHEST SALE
SELECT 
PRODUCTID,
COUNT(SALES) OVER (PARTITION BY PRODUCTID) AS NUMBER_OF_SALES,
SALES,
FIRST_VALUE(Sales) OVER (PARTITION BY PRODUCTID ORDER BY SALES ASC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING ) AS LOWEST_SALE,
LAST_VALUE(SALES) OVER (PARTITION BY PRODUCTID ORDER BY SALES ASC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS HIGHEST_SALE,
SALES - FIRST_VALUE(Sales) OVER (PARTITION BY PRODUCTID ORDER BY SALES ASC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING ) AS DIFF_FROM_LOWEST,
SALES - LAST_VALUE(SALES) OVER (PARTITION BY PRODUCTID ORDER BY SALES ASC ROWS BETWEEN UNBOUNDED PRECEDING AND UNBOUNDED FOLLOWING) AS DIFF_FROM_HIGHEST
FROM SALES.Orders