-- ADD IN SALES STATUS INDICATOR (GREATER THAN 50 HIGH, GREATER THAN 20, MEDIUM, UNDER 20 LOW)
-- SORT SALES HIGH TO LOW
SELECT 
ORDERID,
SALES,
CASE
	WHEN SALES > 50 THEN 'HIGH'
	WHEN SALES > 20 THEN 'MEDIUM'
	ELSE 'LOW'
END AS CATEGORY
FROM SALES.Orders
ORDER BY SALES DESC

-- GROUP BY THE CATEGORY TO VIEW TOTAL SALES PER CATERGORY

SELECT
CATEGORY,
SUM(SALES) AS TOTAL_SALES
FROM (
	SELECT 
	ORDERID,
	SALES,
	CASE
		WHEN SALES > 50 THEN 'HIGH'
		WHEN SALES > 20 THEN 'MEDIUM'
		ELSE 'LOW'
	END AS CATEGORY
	FROM SALES.Orders
)t
GROUP BY CATEGORY
ORDER BY SUM(SALES) DESC


