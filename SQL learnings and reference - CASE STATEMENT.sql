-- RETRIEVE EMPLOYEE DETAILS WITH GENDER DISPLAYED AS FULL TEXT
SELECT 
[EmployeeID],
	[FirstName] ,
	[LastName] ,
	[Department] ,
	[BirthDate] ,
	CASE
	WHEN GENDER = 'M' THEN 'MALE'
	ELSE 'FEMALE'
END AS GENDER,
	[Salary] ,
	[ManagerID]
FROM SALES.Employees

