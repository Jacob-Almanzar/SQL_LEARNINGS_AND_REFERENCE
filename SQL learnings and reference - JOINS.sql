-- CROSS JOIN
-- COMBINES EVERY ROW FROM THE LEFT AND THE RIGHT
-- ALL POSSIBLE COMBINATIONS
SELECT *
FROM customers
CROSS JOIN orders



--JOINING MULTIPLE TABLES
-- USING SALESDB, RETRIEVE A LIST OF ALL ORDERS, ALONG WITH THE RELATED CUSTOMER, PRODUCT, AND EMPLOYEE DETAILS

SELECT * FROM SALES.Orders
SELECT * FROM SALES.Customers
SELECT * FROM SALES.Products
SELECT * FROM SALES.Employees

SELECT 
SALES.Orders.OrderID AS ORDER_ID,
SALES.Customers.FirstName AS CUSTOMER_FIRSTNAME,
SALES.Customers.LastName AS CUSTOMER_LASTNAME,
SALES.Products.Product AS PRODUCT_NAME,
SALES.Orders.Sales AS SALES_AMOUNT,
SALES.Products.Price AS PRODUCT_PRICE,
SALES.Employees.FirstName AS EMPLOYEE_FIRSTNAME,
SALES.EMPLOYEES.LastName AS EMPLOYEE_LASTNAME
FROM SALES.Orders
	LEFT JOIN SALES.Customers
		ON Orders.CustomerID = Customers.CustomerID
	LEFT JOIN SALES.Products
		ON SALES.Orders.ProductID = SALES.Products.ProductID
	LEFT JOIN SALES.Employees
		ON SALES.Orders.SalesPersonID = SALES.Employees.EmployeeID

